package com.eomcs.basic.ex03;
//리터럴
public class Exam1{
  public static void main(String[] args){
//정수리터럴 = 78
System.out.println(78);
System.out.println(+78); //10진수표기법
System.out.println(-78); //10진수표기법

System.out.println(0116); //8진수표기법
System.out.println(-0116); //8진수표기법 출력은 10진수

System.out.println(0x4e); //16진수표기법 
System.out.println(-0x4e); //16진수표기법 

System.out.println(0b01001110); //2진수표기법 

//부동소수점 리터럴
System.out.println(3.14); // 고정소수점
System.out.println(0.314e1); //부동소수점
System.out.println(31.4e-1);

//논리 리터럴
System.out.println(true);
System.out.println(false);

//1 문자리터럴
System.out.println('A');
System.out.println('가');

//문자열 리터럴
System.out.println("안녕하세요. 비트캠프!");





  }
}